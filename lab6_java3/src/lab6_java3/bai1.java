    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
             String header[] = {"Id", "Title", "Price"};
           
 DefaultTableModel tblModel = new DefaultTableModel(header, 0);

        try {
            String userName="sa";
            String password="123";
            String url= "jdbc:sqlserver://localhost:1433;databaseName=Library";
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url, userName, password);
            Statement st = con.createStatement();
            String sql="select * from Books";
            ResultSet rs = st.executeQuery(sql);
            
            tblModel.setRowCount(0);
            
            if(txttitle.getText().length()>0){
                sql = sql + " where title like '%" + txttitle.getText() + "%'";
            }
           
            rs = st.executeQuery(sql);
Vector data = null;
            if(rs.isBeforeFirst()==false)
            {
                JOptionPane.showMessageDialog(this,"the is not avalible!");
                return;
            }
            while(rs.next())
            {
                data = new Vector();
                data.add(rs.getInt("id"));
                data.add(rs.getString("Title"));
                data.add(rs.getString("price"));
                tblModel.addRow(data);
            }
            jTable1.setModel(tblModel);
            
        } catch (Exception e) {
            System.out.println(e);
        }
        finally{
            try {
                if(conn !=null){
                    conn.close();
                }
                if(st!=null){
                    st.close();
                }
                if(rs !=null){
                    rs.close();
                }
                
            } catch (Exception ex) {
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_btnsearchActionPerformed

