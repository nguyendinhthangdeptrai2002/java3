        public void loadform(int i){
           sinhvien sv = list.get(i);        
           txtmasv.setText(sv.getMasv());
           txthoten.setText(sv.getTensv());
           txtngaysinh.setText(sv.getNgaysinh()+"");
           txtsodt.setText(sv.getSodt());
           if(sv.isGioiTinh()==true){// bieesnm dạng boolen is
               rdonam.setSelected(true);
           }
           else{
               rdonu.setSelected(true);
           }
           txtdiachi.setText(sv.getDiachi());
            // dưa dữ liệu từ list lên form theo index
        }
    
