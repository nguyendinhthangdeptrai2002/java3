    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        int count=0;
        for(sinhvien x:list){
            if(txtmasv.getText().equals(x.masv)){
                JOptionPane.showMessageDialog(this, "khoong duoc trung massv");
                count++;
                break;
            }
                }
        if(txtmasv.getText().equals("")|| txthoten.getText().equals("")||txtdiachi.getText().equals("")||txtngaysinh.getText().equals("")||txtsodt.getText().equals(""))
        {
         JOptionPane.showMessageDialog(this,"khong duoc de trong mot truong nao");
         count++;
         
        }
        if(!rdonam.isSelected() && !rdonu.isSelected())
        {
            JOptionPane.showMessageDialog(this, "ban chua chon gioi tinh");
            count++;
        }
        if(count==0){
            
        
        try {
            java.sql.Connection conn = DriverManager.getConnection(url,username,password);   
            String sql ="insert into sinhvien values (?,?,?,?,?,?)";
            PreparedStatement st = conn.prepareStatement(sql);
            
            st.setString(1,txtmasv.getText());
            st.setString(2, txthoten.getText());
            st.setString(3, txtngaysinh.getText());
            st.setString(4, txtsodt.getText());
            int i ;
             if(rdonam.isSelected()){
                 i=1;               
             }
             else{
                 i=0;
             }
             st.setInt(5, i);
             st.setString(6, txtdiachi.getText());
             st.executeUpdate();
             JOptionPane.showMessageDialog(this, "them sinh vien thanh cong");
             conn.close();
             loaddata();
        } catch (Exception e) {
            e.printStackTrace();
        }
       
      }  
    }//GEN-LAST:event_btnsaveActionPerformed

