public boolean  checknull(){
    if(txtfistnumber.getText().equals("")){
        JOptionPane.showMessageDialog(this, "vui lòng nhập fist number");
        txtfistnumber.requestFocus();
        return false ;
    }
    else if(txtscondnumber.getText().equals("")){
        JOptionPane.showMessageDialog(this, "vui lòng nhập Scond number ");
        txtscondnumber.requestFocus();
        return false ;
    }else
      return true;
    
}
