    private void btnsignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsignupActionPerformed
        // TODO add your handling code here:
        if(checknull()){
            String ma;
            ma =txtpass.getText();
            if(!txtconfirm.getText().equals(ma)){
                JOptionPane.showMessageDialog(this, " nhập lại chưa đúng");
                txtconfirm.setText("");
                txtconfirm.requestFocus();
            }
            else{
                  JOptionPane.showMessageDialog(this, " đăng nhập thành công");
            }
          
            
        }
    }//GEN-LAST:event_btnsignupActionPerformed

    
    private boolean checknull(){
        if(txtusename.getText().equals("")){
            JOptionPane.showMessageDialog(this, "không được để trống tên ");
            txtusename.requestFocus();
            return false ;
        }
            if(txtpass.getText().equals("")){
            JOptionPane.showMessageDialog(this, "không được để trống password ");
            txtpass.requestFocus();
            return false ;
        }
                 if(txtconfirm.getText().equals("")){
            JOptionPane.showMessageDialog(this, "vui lòng nhập lại pass ");
            txtconfirm.requestFocus();
            return false ;
        }
            
            return true;
    }
    
