    private void tblbangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblbangMouseClicked
        // TODO add your handling code here:
        int r=tblbang.getSelectedRow();
        if(r<0){
            return;
        }
        txtmasp.setText(tblbang.getValueAt(r, 0).toString());
        txttensp.setText(tblbang.getValueAt(r, 1).toString());
        cbodonvi.setSelectedItem(tblbang.getValueAt(r,2));
       txtdongia.setText(tblbang.getValueAt(r, 3).toString());
       txtnhacungcap.setText(tblbang.getValueAt(r, 4).toString());
    }//GEN-LAST:event_tblbangMouseClicked

