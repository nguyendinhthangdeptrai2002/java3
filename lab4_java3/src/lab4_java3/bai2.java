    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        int ok =0;
        for(int i=0;i<tblbang.getRowCount();i++){
            if(txtmasp.getText().equals(modelProducts.getValueAt(i, 0))){
                JOptionPane.showMessageDialog(this,"san pham da ton tai");
                ok = 1;
            }
        }
        if(ok==0){
        Vector datarow= new Vector();
        datarow.add(txtmasp.getText().trim());
        datarow.add(txttensp.getText().trim());
        datarow.add(cbodonvi.getSelectedItem());
        datarow.add(txtdongia.getText().trim());
        datarow.add(txtnhacungcap.getText().trim());
        modelProducts.addRow(datarow);
        }
    }//GEN-LAST:event_btnaddActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int row = tblbang.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, " chua chon dong nao trong bang");
            return;
        }
        else if(JOptionPane.showConfirmDialog(this, "ban muon xoa that khong")==JOptionPane.OK_OPTION){
            modelProducts.removeRow(row);
        }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        int row = tblbang.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(this,"ban chua chon dong nao");
            return;
        }
        modelProducts.setValueAt(txtmasp.getText().trim(), row,0);
        modelProducts.setValueAt(txttensp.getText().trim(), row,1);
        modelProducts.setValueAt(cbodonvi.getSelectedItem(), row,2);
        modelProducts.setValueAt(txtdongia.getText().trim(), row,3);
        modelProducts.setValueAt(txtnhacungcap.getText().trim(), row,4);
    }//GEN-LAST:event_btnupdateActionPerformed

