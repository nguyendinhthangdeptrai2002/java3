    public void loadDataTojTable() {
        Connection conn = null;
        Statement st = null;
        ResultSet rs = null;
        try {
            conn = DriverManager.getConnection(url);
            String sql = "select * from sufen";
            st = conn.createStatement();
            rs = st.executeQuery(sql);

            tblModel.setRowCount(0);
            if (rs.isBeforeFirst() == false) {
                JOptionPane.showMessageDialog(this, "chưa có sinh viên");
                return;

            }
            while (rs.next()) {
                String gt;
                Vector sv = new Vector();
                sv.add(rs.getString("MASV"));
                sv.add(rs.getString("Hoten"));
                sv.add(rs.getString("Email"));
                sv.add(rs.getString("SoDT"));
                // đây nè
                if (rs.getBoolean("Gioitinh") == true) {
                    gt = "Nam"; 
                } else {
                    gt = "Nữ";
                }
                sv.add(gt);
                sv.add(rs.getString("Diachi"));
                sv.add(rs.getString("Hinh"));
//                 JOptionPane.showMessageDialog(this, sv);
                tblModel.addRow(sv);

            }
            tblbang.setModel(tblModel);
        } catch (Exception e) {
            System.out.println(e);

        } finally {
            try {
                if (conn != null) {
                    conn.close();
                }
                if (st != null) {
                    st.close();
                }
                if (rs != null) {
                    rs.close();
                }
            } catch (Exception exx) {
                System.out.println(exx);
            }
        }
    }

    public void DoDulieulenComponent(int row) {
        if (row < 0) {
            return;
        }
        txtmasv.setText(tblbang.getValueAt(row, 0).toString());
        txthoten.setText(tblbang.getValueAt(row, 1).toString());
        txtemail.setText(tblbang.getValueAt(row, 2).toString());
        txtsodt.setText(tblbang.getValueAt(row, 3).toString());
        String gioitinh = tblbang.getValueAt(row, 4).toString();
        if (gioitinh == "Nam") {
            rdonam.setSelected(true);
        } else {
            rdonu.setSelected(true);
        }
        txtdiachi.setText(tblbang.getValueAt(row, 5).toString());
        uphinh(tblbang.getValueAt(row, 6).toString());

    }

