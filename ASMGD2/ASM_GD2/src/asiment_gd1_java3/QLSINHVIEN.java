        String url = "jdbc:sqlserver://localhost;databaseName=ASMJAVA_3";
        String userv = "sa";
        String pass = "123";
        PreparedStatement ps = null;
        Connection conn = null;
        try {

            String sql = "insert into  STUDENT  (MASV,Hoten,Email,SoDT,Gioitinh,Diachi) VALUES(?,?,?,?,?,?)";

            conn = DriverManager.getConnection(url, userv, pass);
            ps = conn.prepareStatement(sql);
            ps.setString(1, txtmasv.getText());
            ps.setString(3, txtemail.getText());
            ps.setString(2, txthoten.getText());
            ps.setString(4, txtsodt.getText());

            boolean gt;
            if (rdonam.isSelected()) {
                gt = true;
            } else {
                gt = false;
            }
            ps.setBoolean(5, gt);
            ps.setString(6, txtdiachi.getText());
            ps.executeUpdate();

            //
            JOptionPane.showMessageDialog(this, "insert thành công!");
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "insert fail..");
        } finally {
            try {
                if (conn != null) {
                    conn.close();
                }
                if (ps != null) {
                    ps.close();
                }
            } catch (Exception ex) {
                System.out.println(ex);
            }
        }
