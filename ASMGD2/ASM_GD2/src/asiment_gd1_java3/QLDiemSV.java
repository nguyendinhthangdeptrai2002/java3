    public void loaddata(){
        Connection conn = null;
        Statement stmt = null;
        
        try {
            System.out.println("Connection to database...");
            conn = DriverManager.getConnection(url,username,password);
            System.out.println("creating statement...");
            stmt = conn.createStatement();
            String sql="";
            sql = "select ID,DRADE.MASV,Hoten,Tienganh,Tinhoc,GDTC from DRADE, STUDENT where DRADE.MASV = STUDENT.MASV";
            ResultSet rs  = stmt.executeQuery(sql);
            while (rs.next()){  
                int ID = rs.getInt("ID");
                String MASV =rs.getString("MASV");
                String Hoten = rs.getString("Hoten");
                int Tienganh = rs.getInt("Tienganh");
                int Tinhoc  = rs.getInt("Tinhoc");
                int GDTC  =rs.getInt("GDTC");
                
                DiemSV diemSV = new  DiemSV(ID, MASV, Hoten, Tienganh, Tinhoc, GDTC);
                list.add(diemSV);
            }
            currentindex =0;
            DisplayDiemsv();
            rs.close();
            stmt.close();
            conn.close();
        } catch (Exception se) {
            System.out.println(se);
        }
        finally{
            try {
                if(stmt != null){
                    stmt.close();
                }
            } catch (Exception e) {
            }
            try {
                if(conn != null){
                    conn.close();
                }
            } catch (Exception ex) {
                System.out.println(ex);
            }
        }
        System.out.println("done !");
    }
