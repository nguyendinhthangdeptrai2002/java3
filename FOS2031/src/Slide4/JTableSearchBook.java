    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        Connection conn = null;
        Statement st = null;
        ResultSet rs = null;
        try { 
             conn = DriverManager.getConnection("jdbc:sqlserver://localhost;DatabaseName=Library", "sa", "");
             // Câu lệnh xem dữ liệu
             String sql = "select * from Books ";
             // Nếu tìm kiếm theo title
             if (txtTitle.getText().length() > 0) {
               sql = sql + " where title like '%" + txtTitle.getText() + "%'";
             }
             // Tạo đối tượng thực thi câu lệnh Select
             st = conn.createStatement();
             // Thực thi 
             rs = st.executeQuery(sql);
             Vector data = null;
             tblModel.setRowCount(0);
             // Nếu sách không tồn tại
             if (rs.isBeforeFirst() == false) {
                JOptionPane.showMessageDialog(this, "The book is not available!");
                return;
             }
             // Trong khi chưa hết dữ liệu
             while (rs.next()) {
               data = new Vector();
               data.add(rs.getInt("id"));
               data.add(rs.getString("title"));
               data.add(rs.getFloat("price"));
               // Thêm một dòng vào table model
               tblModel.addRow(data);
             }
             jTable1.setModel(tblModel); // Thêm dữ liệu vào table
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
              try {
                if (conn != null) {
                  conn.close();
                }
                if (st != null) {
                 st.close();
                }
                if (rs != null) {
                 rs.close();
                }
               } catch (Exception ex) {
                 ex.printStackTrace();
               }    
        }
        
        
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int ret = JOptionPane.showConfirmDialog(this, "Do you want to delete ID: "+id+" ?", "Confirm", JOptionPane.YES_NO_OPTION);
        if(ret != JOptionPane.YES_OPTION) {
         return;
        }

        Connection c = null;
        PreparedStatement ps = null;

        try {
             c = DriverManager.getConnection("jdbc:sqlserver://localhost;DatabaseName=Library", "sa", "");
             ps = c.prepareStatement("Delete From Books where id = ?");
             ps.setInt(1, id); //giá trị của cột đầu tiên tại dòng được chọn trong table
             ret = ps.executeUpdate();
             if (ret != -1) {
              JOptionPane.showMessageDialog(this, "This book has been deleted"); 
              btnSearchActionPerformed(evt);
             }
        } catch (Exception ex) {
         ex.printStackTrace();
        } finally {
          try {
           if (c != null) {
             c.close();
           }
           if (ps != null) {
             ps.close();
           }
         } catch (Exception ex2) {
           ex2.printStackTrace();
         }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

