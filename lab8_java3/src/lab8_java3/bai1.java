    private void btnsendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsendActionPerformed
        // TODO add your handling code here:
        try {
            Properties p = new Properties();
            p.put("mail.smtp.auth", "true");
            p.put("mail.smtp.starttls.enable", "true");
            p.put("mail.smtp.host", "smtp.gmail.com");
            p.put("mail.smtp.port", 587);
          String user = txtname.getText();// tk email
          String pass = txtpass.getText();//pass email
          //tạo một đối tượng secssion
           Session s = Session.getInstance(p,
                    new javax.mail.Authenticator() {
                        protected PasswordAuthentication getPasswordAuthentication() {
                            return new PasswordAuthentication(user, pass);
                        }});   
           
           String from =txtname.getText();
           String to = txtnguoinhan.getText();
           String subject = txtsubiect.getText();
           String body = txtmesage.getText();
            Message msg = new MimeMessage(s);
             msg.setFrom(new InternetAddress(from));
             msg.setRecipients(Message.RecipientType.TO, InternetAddress.parse(to));
          msg.setSubject(subject);
          msg.setText(body);
            Transport.send(msg);
            msg.setSentDate(new Date());
            
                MimeBodyPart textPart = new MimeBodyPart();
            textPart.setContent(body, "text/plain");
            Multipart mp = new MimeMultipart();
            mp.addBodyPart(textPart);          
            String strAttach   = txturlfile.getText().trim();
            if (!strAttach.equals(""))
            {                                    
                MimeBodyPart attachFilePart = new MimeBodyPart();
                FileDataSource fds = new FileDataSource(strAttach);
                attachFilePart.setDataHandler(new DataHandler(fds));
                attachFilePart.setFileName(fds.getName());
                mp.addBodyPart(attachFilePart);
            }
                          
            msg.setContent(mp);
            
            Transport.send(msg);
            JOptionPane.showMessageDialog(null, "Mail was sent successfully.", "Message", 
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }catch (Exception ex) {
            System.out.println(ex);
        }
        
    }//GEN-LAST:event_btnsendActionPerformed

